/**
 * 7. Write a Java program to print the sum of two numbers.
 * Test Data: 74 + 36
 * Expected Output: 110
 */

public class Program7_SumOfTwoNumbers {
    public static void main(String[] args) {
        // The two integers are a & b
        int a = 74;
        int b = 36;
        int add = a + b;
        //Concatenation
        System.out.println("The Sum of" + " " + a + " " + "and" + " " + b + " " + "is" + " " + add);
    }
}
