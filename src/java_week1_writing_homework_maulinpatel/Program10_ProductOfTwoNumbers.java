package java_week1_writing_homework_maulinpatel;

/**
 * 10. Write a Java program that takes two numbers as input and display the
 * product of two numbers.
 * Test Data:
 * Input first number: 25
 * Input second number: 5
 * Expected Output : 25 x 5 = 125
 */
public class Program10_ProductOfTwoNumbers {

    public static void main(String[] args) {
        // First number declaration
        int first = 25;
        // Second number declaration
        int second = 5;
        int product = first * second;
        //Expected Output
        System.out.println(first + " X " + second + " = " + product);
    }
}
