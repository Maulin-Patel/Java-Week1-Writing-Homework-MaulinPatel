package java_week1_writing_homework_maulinpatel;

/**
 * 8. Write a Java program to divide two numbers and print on the screen.
 * Test Data: 50/3
 * Expected Output: 16
 */
public class Program8_DivisionOfTwoNumbers {
    public static void main(String[] args) {
        // The two integers are a & b
        int a = 50;
        int b = 3;
        int div = a / b;
        // Concatenation
        System.out.println(a + " Divided By " + b + " is " + div);
    }
}
