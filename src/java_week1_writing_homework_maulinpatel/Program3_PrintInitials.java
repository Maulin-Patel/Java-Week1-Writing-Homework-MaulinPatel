package java_week1_writing_homework_maulinpatel;

/**
 * 3. Write a program that will print your initials to standard output in letters that
 * are nine lines tall. Each big letter should be made up of a bunch of *'s.
 */
public class Program3_PrintInitials {
    public static void main(String[] args) {
        System.out.println("**     **   ********    ********");
        System.out.println("** * * **   **    **    **    ** ");
        System.out.println("**  *  **   **    **    **    **");
        System.out.println("**     **   **    **    ******** ");
        System.out.println("**     **   **    **    **       ");
        System.out.println("**     **   **    **    **    ");
        System.out.println("**     **   **    **    **    ");
        System.out.println("**     **   ********    **    ");

    }
}
